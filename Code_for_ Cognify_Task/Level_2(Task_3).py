import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import plotly.express as px
# Load the dataset
df = pd.read_csv('Dataset .csv')


# Extract additional features from the existing columns, such as the length of the restaurant name or address.
# ðŸ”¹ Create a new feature: Length of restaurant name
df['Restaurant Name Length'] = df['Restaurant Name'].astype(str).apply(len)

# ðŸ”¹ Create another feature: Length of address (if present)
df['Address Length'] = df['Address'].astype(str).apply(len)

# Display the new features
print("ðŸ”¹ Sample of New Features:\n")
print(df[['Restaurant Name', 'Restaurant Name Length', 'Address', 'Address Length']].head())


#Create new features like "Has Table Booking" or "Has Online Delivery" by encoding categorical variables.

# ðŸ”¹ Encode 'Has Table booking': Yes â†’ 1, No â†’ 0
df['Has Table Booking (Binary)'] = df['Has Table booking'].apply(lambda x: 1 if x == 'Yes' else 0)

# ðŸ”¹ Encode 'Is delivering now': Yes â†’ 1, No â†’ 0
df['Has Online Delivery (Binary)'] = df['Is delivering now'].apply(lambda x: 1 if x == 'Yes' else 0)

# ðŸ”¸ Show the new columns
print("ðŸ”¹ Encoded Features Sample:\n")
print(df[['Has Table booking', 'Has Table Booking (Binary)', 'Is delivering now', 'Has Online Delivery (Binary)']].head())

