import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# Load the dataset
df = pd.read_csv('Dataset .csv')
#Calculate basic statistical measures (mean,median, standard deviation, etc.) for numerical columns
# Convert necessary columns to numeric (in case they are object types)
df['Aggregate rating'] = pd.to_numeric(df['Aggregate rating'], errors='coerce')
df['Votes'] = pd.to_numeric(df['Votes'], errors='coerce')
df['Price range'] = pd.to_numeric(df['Price range'], errors='coerce')

# Get basic statistical summary
print("ðŸ”¹ Basic Statistical Measures for Numerical Columns:\n")
print(df.describe())

# Get median separately (since it's not included in .describe())
print("\nðŸ”¹ Median Values:\n")
print(df.median(numeric_only=True))

# Explore the distribution of categorical variables like "Country Code," "City," and "Cuisines".
# ðŸ”¹ View top 10 most frequent Country Codes
print("ðŸ”¹ Top 10 Country Codes:\n")
print(df['Country Code'].value_counts().head(10))

# ðŸ”¹ View top 10 most common Cities
print("\nðŸ”¹ Top 10 Cities:\n")
print(df['City'].value_counts().head(10))

# ðŸ”¹ View top 10 most common Cuisines
print("\nðŸ”¹ Top 10 Cuisines:\n")
print(df['Cuisines'].value_counts().head(10))

# Optional: Plot the top 10 cities
plt.figure(figsize=(10,5))
sns.countplot(y='City', data=df, order=df['City'].value_counts().head(10).index, palette='magma')
plt.title('Top 10 Cities with Most Restaurants')
plt.xlabel('Number of Restaurants')
plt.ylabel('City')
plt.tight_layout()
plt.show()

# Identify the top cuisines and cities with the highest number of restaurants
# ðŸ”¹ Top 10 cities with the highest number of restaurants
print("ðŸ”¹ Top 10 Cities with Most Restaurants:\n")
top_cities = df['City'].value_counts().head(10)
print(top_cities)

# ðŸ”¹ Top 10 cuisines served by most restaurants
print("\nðŸ”¹ Top 10 Cuisines by Restaurant Count:\n")
top_cuisines = df['Cuisines'].value_counts().head(10)
print(top_cuisines)

# ðŸ”¸ Optional: Plot Top Cities
plt.figure(figsize=(10, 5))
sns.barplot(x=top_cities.values, y=top_cities.index, palette='plasma')
